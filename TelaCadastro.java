    private void salvarCadastro() throws Exception{
        var Ra = RA.getText();
        var usuario = nome.getText();
        var password = senha.getText();
        String query = "INSERT INTO usuarios (RA, nome, senha) VALUES (?, ?, ?)";
        try(
                Connection conexao = ConnectionFactory.obterConexao(); PreparedStatement psQuery = conexao.prepareStatement(query)){
            psQuery.setString(1, Ra);
            psQuery.setString(2,usuario);
            psQuery.setString(3,password);
            
            int registrosInseridos = psQuery.executeUpdate();
            
            if(registrosInseridos > 0){
                System.out.println("Dados inseridos.");
            } else{
                System.out.println("Erro ao inserir os dados.");
            }
            
            psQuery.close();
            conexao.close();
        }
    }
